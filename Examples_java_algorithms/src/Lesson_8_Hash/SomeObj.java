package Lesson_8_Hash;

public class SomeObj {

    private int id;
    private String name;

    public SomeObj(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}


